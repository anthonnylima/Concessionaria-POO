package Concessionaria;

public class Carro {
    String modelo;
    int anoCarro;
    String marca;
    double valor;
    String placa;

    public Carro(String modelo, int anoCarro, String marca, double valor, String placa) {
        this.modelo = modelo;
        this.anoCarro = anoCarro;
        this.marca = marca;
        this.valor = valor;
        this.placa = placa;
    }
}
